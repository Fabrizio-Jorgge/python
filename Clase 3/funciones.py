

def sumar (a, b):
    """Suma dos números y devuelve el resultado."""
    return a + b

resultado = sumar(5, 3)
print("resultado de la suma:", resultado)

def restar(a, b):
    """Resta dos números y devuelve el resultado."""
    return a - b

resultado_resta = restar(10, 4)
print("resultado de la resta:", resultado_resta)    
